    @Override
    public void write() {
        if (tglAdd.isSelected()) {
            for (String id: centreDict.keySet()) {
                if (id.equals(txtCID.getText())) {
                    PopupDialog.error("Duplicate centre ID");
                    return;
                }
            }
            Address addr = new Address(txtCStreet.getText(),
                    txtCCity.getText());
            Centre newC = new Centre(txtCID.getText(),
                    txtCName.getText(), addr);
            newC.initStock();
            centreDict.put(txtCID.getText(), newC);
        }
        else {
            Centre selected = centreDict.get(txtCID.getText());
            selected.setName(txtCName.getText());
            Address addr = new Address(txtCStreet.getText(),
                    txtCCity.getText());
            selected.setAddr(addr);
        }
        Centre[] cenArr = centreDict.values().toArray(new Centre[0]);
        Transfer.exportObject(cenArr, Centre.FILENAME);
    }

    @Override
    public void fillDetails() {
        // pointer == -1 when the table is disabled but clicked
        if (pointer != -1) {
            txtCID.setText((String) tblCentre.getValueAt(pointer, 0));
            txtCName.setText((String) tblCentre.getValueAt(pointer, 1));
            txtCStreet.setText((String) tblCentre.getValueAt(pointer, 2));
            txtCCity.setText((String) tblCentre.getValueAt(pointer, 3));
            cleared = false;
        }
    }
    
