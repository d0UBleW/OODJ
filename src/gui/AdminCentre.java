    public String check() {
        if (!Checker.isCentreID(txtCID.getText()) || txtCName.getText().isBlank() ||
                txtCStreet.getText().isBlank() || txtCCity.getText().isBlank()) {
            return "Please do not leave text field in blank";
        }
        char[] bad = {
            ';',
            '_',
        };
        if (!Checker.hasNoBadChar(txtCName.getText(), bad))
            return "Bad character in centre name";
        if (!Checker.hasNoBadChar(txtCStreet.getText(), bad))
            return "Bad character in centre street";
        if (!Checker.hasNoBadChar(txtCCity.getText(), bad))
            return "Bad character in centre city";
        return null;
    }

