    private void radUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_radUserActionPerformed
        // TODO add your handling code here:
        if (radUser.isSelected()) {
            role = "User";
            lblID.setText("IC/Passport Number");
            lblTitle.setText("User Login");
        }
        else {
            boolean authorized = PopupDialog.prompt("Input secret key", true, "letmein");
            if (authorized) {
                role = "Admin";
                lblID.setText("Admin ID");
                lblTitle.setText("Admin Login");
            }
            else {
                PopupDialog.error("Wrong key");
                radUser.setSelected(true);
            }
        }
    }//GEN-LAST:event_radUserActionPerformed

