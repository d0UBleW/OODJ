    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String ID = txtICPNum.getText();
        String password = String.valueOf(passwd.getPassword());
        Account acc;
        if (role.equals("Admin")) {
            acc = new Personnel(ID, password);
        }
        else {
            if (ID.length() == 8 && ID.substring(0, 2).equals("IC")) {
                acc = new Citizen(ID, password);
            }
            else if (ID.length() == 7 && ID.substring(0, 1).equals("P")){
                acc = new NonCitizen(ID, password);
            }
            else {
                acc = null;
            }
        }
        boolean loginStatus = Auth.login(acc);
        if (!loginStatus) {
            PopupDialog.error("Login failed");
            return;
        }

        if (role.equals("Admin")) {
            this.dispose();
            new AdminPeople().setVisible(true);
        }
        else {
            this.dispose();
            new UserAccount((People) acc).setVisible(true);
        }

    }//GEN-LAST:event_btnLoginActionPerformed

